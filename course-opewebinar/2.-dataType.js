//String
// ● Cadena de caracteres 
// ○ Que representan texto 
// ○ Se usan las comillas “(dobles)” y ‘(simples)’ para representarlo en el código. 
// ○ Pueden contener cualquier tipo de texto 
let address = 'Calle de la via concurrida 9';

//NUMBER 
// ● Dato numérico ○ Representan un número 
// ○ Cumplen el estándar IEEE 754 de 64 bits 
// ○ Almacena valores como mínimo de y como máximo 
// ○ Los numero que excedan ese límite se convierten a Infinity 
// ○ “NaN” es un número especial cuando no se puede expresar una expresión aritmética como un número 
let price = 185;

//BOOLEAN 
// ● Dato lógico ○ Se representa con dos valores: true o false ○ Es usado es condicionales 
// ○ También en bucles ○ Como valor de retorno de una función 
let userActive = true;

//UNDEFINED 
// ● Valor por defecto 
// ○ Lo asigna automáticamente JavaScript a variables que solo se han declarado, pero no tienen valor asignado 
let ourValue;

//NULL 
// ● Valor referenciado 
// ○ Es un valor que usamos y usaremos de forma intencionada para indicar que una variable tiene un valor inexistente y que no existe siquiera su referencia en memoria 
let otherValueNull = null;


